package ifsc;

public class Veiculo {
	
	private String nome;
	private Integer ano;
	private Integer nroda;
	private String fabri;
	private String cor;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Integer getAno() {
		return ano;
	}
	public void setAno(Integer ano) {
		this.ano = ano;
	}
	public Integer getnRoda() {
		return nroda;
	}
	public void setnRoda(Integer nRoda) {
		this.nroda = nRoda;
	}
	public String getFabri() {
		return fabri;
	}
	public void setFabri(String fabri) {
		this.fabri = fabri;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	
	
}
